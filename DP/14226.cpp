#include <iostream>
#include <vector>
#include <queue>

#define INF 987654321
using namespace std;
//boj 이모티콘 https://www.acmicpc.net/problem/14226

typedef pair<int, int> pp;
typedef pair<int, pp> ipp;
bool dp[1001][1001];
int ans = INF;
int s;
int min(const int &a, const int &b)
{
    if (a < b)
        return a;
    return b;
}
void bfs()
{
    queue<ipp> q;
    q.push(ipp(0, pp(1, 0)));
    dp[1][0] = true;

    while (!q.empty())
    {
        int now = q.front().second.first;
        int buffer = q.front().second.second;
        int cnt = q.front().first;
        q.pop();

        if (now == s)
        {
            ans = min(cnt, ans);
            return;
        }

        if (now - 1 >= 2 && !dp[now - 1][buffer])
        {
            dp[now - 1][buffer] = true;
            q.push(ipp(cnt + 1, pp(now - 1, buffer)));
        }
        if (!dp[now][now])
        {
            //복사
            dp[now][now] = true;
            q.push(ipp(cnt + 1, pp(now, now)));
        }
        if (now + buffer < 1001 && !dp[now + buffer][buffer])
        {
            //붙여넣기
            dp[now + buffer][buffer] = true;
            q.push(ipp(cnt + 1, pp(now + buffer, buffer)));
        }
    }
}
int main()
{

    ios_base::sync_with_stdio(0);
    cin.tie(0);

    cin >> s;

    bfs();
    cout << ans << '\n';
    return 0;
}
